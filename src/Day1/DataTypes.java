package Day1;

public class DataTypes {
	public static void main(String[] args) {
		int a = 24;
		int b = 13;
		
		System.out.println("integer divison: " + a/b);
		System.out.println("float divison: " + (float)a/b);  // typecasting syntax: (datatype) expression or variable
		System.out.println("double divison: " + (double)a/b);

	}

}


